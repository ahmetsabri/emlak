        if (blog_size == 3) {
            $(this).closest('.context').children('.wrapper').find('p').css('font-size', '1.75rem')
        }

    });

    var copy_interval;
    $('.blog-single .container .context .content .support div.copy').click(function() {
        $(this).css('pointer-events', 'hidden');
        $(this).addClass('copied');
        navigator.clipboard.writeText(window.location.href);
        clearInterval(copy_interval);

        copy_interval = setInterval(() => {
            $(this).css('pointer-events', 'unset');
            $(this).removeClass('copied');
        }, 1000);
    });

    $('.blog-single .container .context .content .support div.print').click(function() {
        $('.blog-single .container .context > .wrapper').printThis();
    });
